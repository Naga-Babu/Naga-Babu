
public class Statement1 {
public static void main(String[] args) {
	char x = 'F';
	switch(x)
	{
	case'a':
	case'A':
		System.out.println("Value of x is a");
		break;
	case'b':
	case'B':
		System.out.println("Value of x is b");
		break;
	case'c':
	case'C':
		System.out.println("Value of x is c");
		break;
		default:
		System.out.println("Value of x is a,b,c");
		break;
		
	}
}
}
